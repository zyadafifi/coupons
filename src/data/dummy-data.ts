export const getStoreById = (id: string): Store | undefined => 
  stores.find(store => store.id === id);

export const getCategoryById = (id: string): Category | undefined => 
  categories.find(category => category.id === id);

export const getCouponsByStore = (storeId: string): Coupon[] => 
  coupons.filter(coupon => coupon.storeId === storeId);

export const getCouponsByCategory = (categoryId: string): Coupon[] => 
  categoryId === 'c1' ? coupons : coupons.filter(coupon => coupon.categoryId === categoryId);
